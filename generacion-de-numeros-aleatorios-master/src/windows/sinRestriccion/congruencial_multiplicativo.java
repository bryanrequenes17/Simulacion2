    /**
     * Metodo que llena el componete JTable con los valores que se requieran, ya
     * sean todos los generados o solo los del periodo.
     *
     * @param size {boolean} bandera que define si se reduce o no la tabla.
     */
    private void cargarTabla(boolean size) {
        String Titulo[] = {"N", "Xn", "(aXn + c) / m", "Xn+1", "NÃºmeros Uniformes"};
        String registros[] = new String[5];
        DefaultTableModel modelo = new DefaultTableModel(null, Titulo);
        x0 = Integer.parseInt(txt_Xn.getText().trim());
        k = Integer.parseInt(txt_a.getText().trim());
        d = Long.parseLong(txt_m.getText().trim());
//        d = 4294967296L;
        while (x0 <= 0) {
            x0 = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el valor de la semilla X0"));
        }
        while (k <= 0) {
            k = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el valor de k"));
        }
        while (d <= 0) {
            d = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el valor de d"));
        }
        Controller.sinRestriccion.Congruencial_Multiplicativo cm = new Controller.sinRestriccion.Congruencial_Multiplicativo();
        ArrayList array = cm.getArray(x0, k, d, 0, type);
        int[] values = cm.getValues();
        txt_Xn.setText(String.valueOf(values[5]));
        txt_Period.setText(String.valueOf(values[1]));
        int tam = (size) ? values[1] : array.size();
        for (int i = 0; i < tam; i++) {
            Data data = (Data) array.get(i);
            registros[0] = String.valueOf(data.getN());
            registros[1] = String.valueOf(data.getXn());
            //+ div + " + " + xn1 + "/" + m +
            registros[2] = data.getDivicion() + " + (" + data.getXn1() + " / " + values[2] + ")";
            registros[3] = String.valueOf(data.getXn1());
            registros[4] = String.valueOf(data.getDecimales());
            modelo.addRow(registros);
        }
        tbl_Resultados.setModel(modelo);
    }

